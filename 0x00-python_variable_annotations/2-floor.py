def floor(n: float) -> int:
    """
    Type-Annotated function: floor().

    Takes an argument of type float, floors it and returns the floored
    argument as an int.

    Args:
            n (float): Float argument to be floored

    Returns:
            Floor value of n (int).
    """
    return int(n)
