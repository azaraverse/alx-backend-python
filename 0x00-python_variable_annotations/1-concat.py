#!/usr/bin/env python3
"""
Type-Annotated function: concat()
"""


def concat(str1: str, str2: str) -> str:
    """
    Type-Annotated function: concat().

    Takes two arguments as strings, concatenates them and
    returns them as a single, typed string.

    Args:
            str1 (str): First string argument.
            str2 (str): Second string argument.

    Returns:
            A concatenated string.
    """
    return str1 + str2
