def add(a: float, b: float) -> float:
    """
    Type-Annotated function: add().
    Takes two floats as arguments and returns their sum as a
    float.

    Args:
            a (float): Variable a of type float
            b (float): Variable b of type float

    Returns:
            Sum of a and b of type float.
    """
    return a + b
